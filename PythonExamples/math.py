#!/usr/bin/python

x = 5
y = 3

mySum = x + y
print(str(x)+ " + "+str(y)+" = "+str(mySum))

difference = x - y
print(str(x)+" - "+str(y)+" = "+str(difference))

product = x * y
print(str(x)+ " * "+str(y)+" = "+str(product))

quotient = x / y
print(str(x)+ " / "+str(y)+" = "+str(quotient))

floor = x // y
print(str(x)+ " // "+str(y)+" = "+str(floor))

remainder = x % y
print(str(x)+ " % "+str(y)+" = "+str(remainder))

power = x ** y
print(str(x)+ " ** "+str(y)+" = "+str(power))

negative = -x
print("-"+str(x)+" = "+str(negative))
